/**
 * Takes the enriched analysis object (keyed by userId) and upserts
 * each user's data as a separate document into MongoDB.
 */
export async function insertUserAnalysis(analysisResult) {
  const ops = Object.values(analysisResult).map((userData) => ({
    updateOne: {
      filter: { userId: userData.userId },
      update: { $set: { ...userData, updatedAt: new Date() } },
      upsert: true,
    },
  }));

  if (ops.length === 0) {
    console.log("No user data to insert.");
    return;
  }

  const result = await UserAnalysis.bulkWrite(ops);
  console.log(
    `MongoDB: ${result.upsertedCount} inserted, ${result.modifiedCount} updated out of ${ops.length} users.`,
  );
  return result;
}

